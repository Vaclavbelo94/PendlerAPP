export type CentralizedLanguage = 'cs' | 'de' | 'pl';

// Funkce pro získání překladu
export const getCentralizedTranslation = (language: CentralizedLanguage, key: string): string => {
  const translations = centralizedTranslations[language];
  const translation = translations[key as keyof typeof translations];
  
  if (!translation) {
    console.warn(`Translation missing for key: ${key} in language: ${language}`);
    return key;
  }
  
  return translation;
};
